var brandsCheckboxes = Array.from(document.querySelectorAll(".brands"));
var colorCheckboxes = Array.from(document.querySelectorAll(".color"));
var priceCheckboxes = Array.from(document.querySelectorAll(".price"));

function updateDisplay() {
  // Get selected brand values
  var selectedBrands = [];
  brandsCheckboxes.forEach(function (checkbox) {
    if (checkbox.checked) {
      selectedBrands.push(checkbox.value);
    }
  });

  // Get selected color values
  var selectedColors = [];
  colorCheckboxes.forEach(function (checkbox) {
    if (checkbox.checked) {
      selectedColors.push(checkbox.value);
    }
  });

  // Get selected price range
  const selectedPriceRange = Array.from(
    document.querySelectorAll(".price:checked")
  ).map((checkbox) => checkbox.value);

  function getPriceRangeValues(priceRange) {
    const [min, max] = priceRange.split("-").map((value) => parseInt(value));
    return { min, max };
  }

  // Filter products based on selected options
  var filteredProducts = sofaData.filter((product) => {
    var brandMatch =
      selectedBrands.length === 0 || selectedBrands.includes(product.brand);
    var colorMatch =
      selectedColors.length === 0 || selectedColors.includes(product.color);
    // var productPrice = parseFloat(selectedPriceRange.price.replace("$", "").trim());

    if (selectedPriceRange.length === 0) {
      return brandMatch && colorMatch;
    } else {
      return (
        brandMatch &&
        colorMatch &&
        selectedPriceRange.some((range) => {
          const priceRangeValues = getPriceRangeValues(range);
          const productPrice = parseFloat(
            product.price.replace("$", "").replace(",", "").trim()
          );
          return (
            productPrice >= priceRangeValues.min &&
            productPrice <= priceRangeValues.max
          );
        })
      );
    }
  });

  // Update the displayed products (you should implement this part)
  // You might need to have an HTML element where you can display the filtered products.
  display(filteredProducts);
}

